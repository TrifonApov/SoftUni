using System;

class DeclareVariables
{
    static void Main()
    {
        ushort firstNum = 52130;
        sbyte secondNum = - 115;
        int thirdNum = 4825932;
        byte fourthNum = 97;
        short fifthNum = -10000;

        Console.WriteLine(firstNum + " - ushort");
        Console.WriteLine(secondNum + " - sbyte");
        Console.WriteLine(thirdNum + " - int");
        Console.WriteLine(fourthNum + " - byte");
        Console.WriteLine(fifthNum + " - short");
    }
}
